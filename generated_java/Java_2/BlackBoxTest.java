import java.util.*;
import java.lang.*;

public class BlackBoxTest {
    public static void main(String[] args) {
        BaseTest.main(args);
        Solution s = new Solution();
        checkClose(s.truncateNumber(7.0), 0.0, "integer input has zero decimal part");
        checkClose(s.truncateNumber(0.125), 0.125, "fraction-only positive number returns itself");
        checkClose(s.truncateNumber(123456.789), 0.789, "large positive decimal keeps fractional part");    }

    private static void check(boolean condition, String message) {
        if (!condition) {
            throw new AssertionError(message);
        }
    }

    private static void checkClose(double actual, double expected, String message) {
        if (Math.abs(actual - expected) > 1e-6) {
            throw new AssertionError(message + " expected=" + expected + " actual=" + actual);
        }
    }
}