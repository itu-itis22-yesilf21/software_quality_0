import java.util.*;
import java.lang.*;

public class BlackBoxTest {
    public static void main(String[] args) {
        BaseTest.main(args);
        Solution s = new Solution();
        check(s.allPrefixes("").equals(Arrays.asList()), "empty string has no prefixes");
        check(s.allPrefixes("x").equals(Arrays.asList("x")), "single-character string has one prefix");
        check(s.allPrefixes("ab").equals(Arrays.asList("a", "ab")), "two-character string has two prefixes");    }

    private static void check(boolean condition, String message) {
        if (!condition) {
            throw new AssertionError(message);
        }
    }

    private static void checkClose(double actual, double expected, String message) {
        if (Math.abs(actual - expected) > 1e-6) {
            throw new AssertionError(message + " expected=" + expected + " actual=" + actual);
        }
    }
}